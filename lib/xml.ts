// Shared XML escaping for the feed and sitemap route handlers. Both emit
// hand-built XML, so the escape rules live in one place.
export function escapeXml(unsafe: string): string {
  return unsafe
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&apos;");
}
